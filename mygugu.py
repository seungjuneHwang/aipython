# 숫자 두개를 입력을 받아서
# +, -, *, / 를 출력 하는 프로그램을 만들어 보자
# 사용자에게 숫자 두 개를 입력받습니다 (문자열을 int 변환)
a = int(input("첫 번째 숫자를 입력하세요: "))
b = int(input("두 번째 숫자를 입력하세요: "))

# 덧셈, 뺄셈, 곱셈, 나눗셈을 수행하고 출력
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")

# 나눗셈의 경우, 분모가 0이 아닐 때만 수행
if b != 0:
    print(f"{a} / {b} = {a / b}")
else:
    print("0으로 나눌 수 없습니다.")